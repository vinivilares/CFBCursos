let curso = "Curso de Javascript moderno"

// startsWith retorna true caso a string comece com o valor indicado
console.log(curso.startsWith("Curso") ? "Verdadeiro" : "Falso")

// endsWith retorna true caso a string termine com o valor indicado
console.log(curso.endsWith("moderno"))

// includes retorna true caso a string contenha o valor indicado
console.log(curso.includes("de"))

// repeat repete uma string um numero determinado de vezes
console.log(curso.repeat(2))